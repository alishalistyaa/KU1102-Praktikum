# Program
# Mencari keliling lingkaran

# KAMUS
# r = float (input jari-jari)
# pi = float
# k = float (output keliling)

# Input
import math
r = float(input("Masukkan jari-jari lingkatan: "))

# Algoritma Pengolahan
k = 2*math.pi*r

# Mencetak hasil ke layar
print(f"Keliling lingkarannya adalah {k}.")